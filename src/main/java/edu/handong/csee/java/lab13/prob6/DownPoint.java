package edu.handong.csee.java.lab13.prob6;

public class DownPoint {

	private int x, y;
	
	DownPoint(int x, int y){ // constructor to initialize with a specific value
		this.x = x;
		this.y = y;
	}
	public String toString() // simple method printing sentence
	{
		return "x : " + x + "  y : " + y;
		
	}

}
